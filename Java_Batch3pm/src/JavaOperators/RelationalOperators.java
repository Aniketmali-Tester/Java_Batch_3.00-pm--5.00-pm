package JavaOperators;

public class RelationalOperators {

	public static void main(String[] args) {
		//Declare variable
		
		int a=10;
		double b=10;
		
		
		
		System.out.println(a==b);//True
		System.out.println(a!=b);//False
		System.out.println(a>b);//false
		System.out.println(a<b);//false
		System.out.println(a>=b);//True
		System.out.println(a<=b);//True

	}

}
